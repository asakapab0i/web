<p>&quot;I was enraptured by what I saw taking place on stage. The design of the puppets is magnificent, each of them embodying their character as easily as a live person. The puppets&#39; costumes are precise but not overstated, resulting in the puppets settling into the world created for them perfectly. ... <em>The Fortune Teller</em> is easily more interesting, entertaining, and enjoyable than anything I&#39;ve seen in a movie theater all year.&quot;</p>

