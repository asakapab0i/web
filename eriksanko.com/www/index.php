<p><em><strong>Backstage:</strong></em></p>

<p>"Erik Sanko's marvelously creepy marionette play... [is] a perfect pre-Halloween morality play with a twist ending worthy of <em>The Twilight Zone</em>.&nbsp; Even as the play induces tingles and bemused smiles, the craftsmanship of the entire production astounds... [a] grandly satisfying piece of theatre in minature."</p>
<p><a href="/about/press/FortuneTellerPress/backstageFTreview.pdf"><img src="/pdf.gif" width="16" height="16" border="0"/></a>&nbsp;<a href='backstageFTreview.pdf' title='file #2673 (62KB application/pdf)' nolink="" >Backstage Review Fortune Teller</a>&nbsp;</p>
