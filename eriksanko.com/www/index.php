<p><a href="http://www.villagevoice.com/theater/0644,soloski,74874,11.html" target="_blank" title="Fortune Teller Voice Review"><em><strong>The Village Voice:</strong></em></a>&nbsp; &quot;...a repulsively refulgent marionette show featuring the seven deadly sins...&nbsp; [Sanko] and erstwhile Oingo Boingo frontman Danny Elfman offer a delightfully eerie score that tickles and alarms.&nbsp; Irish songster Gavin Friday lends his gravelly tones to the narration.&nbsp; The design team has built costumes and sets with any number of sinister trims and frills.&nbsp; Indeed, for those souls with a taste for the elegantly macabre, attendance is highly advised.&nbsp; To miss it—now that would be a sin.&quot;&nbsp;</p>

<p><a href="http://theater2.nytimes.com/2006/10/30/theater/reviews/30fort.html" target="_blank" title="Fortune Teller Times Review"><em><strong>The New York Times:</strong></em></a>&nbsp; &quot;A morality fable for grown-ups, evoking the familiar idioms of Edward Gorey and Tim Burton in a style you might term Victorian ghastly... the set, a regular wunderkammer, keeps opening up to reveal new sets and images, little Victorian dioramas... macabre and engaging.&quot;&nbsp;&nbsp;</p>
<p><em><strong>The New York Times:</strong></em>&nbsp; &quot;Mr. Sanko&#39;s figures are the grim spawn of Edward Gorey and David Lynch, with papier-mache faces more grizzled and world-weary than those of most character actors. &#39;Very few puppet theaters take advantage of their creepy factor,&#39; Mr. Sanko said... The set underscores why <em>The Fortune Teller</em> is an anomaly in the sphere of marionette theater, or any theater: a level of intricacy most commonly seen in fine art.&quot;</p>
