glasses, antique red velvet rope, a beautiful woman named Raven
attending to the list, each time a guest arrived their name was whited
out so by the end of the evening there was no evidence of any guests
having arrived. I guess the 72 empty bottles of Lanson champagne (our
fearless sponsor) gave us some proof! Paintings #2 and #4 are sold.
