<h1>Paintings</h1>
One night last winter, I had a dream of a completed painting. I woke
up and make a quick sketch and wrote down the materials: chalkboard
paint, chalk, and beeswax and promptly forgot about it. One week
later, I went out to my studio and my friend who I rent my space from
said- hey you guys don't have any use for this can of chalkboard paint
do you, I'm going to throw it away. I swallowed hard and said, yes!
I immediately went out and got some 4x4 panels and started, it was
predetermined.
<br><br>
The end result- a grouping of seven almost totemic paintings (my first
ever) all from chalkboard paint, chalk, tissue paper and beeswax.
 My first showing of them - May 2005- was in Brooklyn in a broken down
industrial space turned gallery for the evening.  Paintings 
