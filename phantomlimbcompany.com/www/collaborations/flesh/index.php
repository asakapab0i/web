<i>Flesh and Blood and Fish and Fowl</i> is a haunting and comedic piece about the limited perspective that humans have of the world and their relationship to wilderness, wildlife and time. Rendered in a surreal story-book style using homespun stop-motion animation and puppetry with taxidermied animals, this is a dark yet magical piece that captures the myopia of our contemporary culture as it stands teetering on the edge of oblivion. It is a kind of doomsday fairytale, where winters turn to summers, shadows no longer behave, dead animals come to life, and the "order of the food chain" no longer applies.
<br><br>
Phantom Limb Company collaborated on this production, which <a href="http://www.livearts-fringe.org/2008/details.cfm?id=2877">opened 9/9/2008 as part of the 2008 Philadelphia Live Arts Festival</a>, through set, puppet and prop design.  
