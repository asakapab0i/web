<p>A collaboration with <a href="http://www.pingchong.org/">Ping Chong &amp; Company</a> to develop an adaptation of <i>The Devil and Daniel Webster</i> through set design and puppet design.<br><br></p>

<h3><a href="/collaborations/wall/">The Wall</a>, November 2008</h3>
<p>Phantom Limb collaborated with Urlike Quade in Holland to create <i>The Wall</i>, a theater piece combining puppetry, music, dance and visual art.<br><br></p>

<h3><a href="/collaborations/flesh/">Flesh and Blood Fish and Fowl</a>, September 2008</h3>
<p><i>Flesh and Blood and Fish and Fowl</i> is a haunting and comedic piece about the limited perspective that humans have of the world and their relationship to 
wilderness, wildlife and time. <br><br></p>

<h3><a href="/collaborations/speaking/">Speaking In Tongues</a>, September 2007</h3>
<p>Set design for the Danish premiere of Andrew Bovell's play, <i>Speaking in Tongues</i> (produced by Eklektisk Teaterproduktion and Crossways).<br><br></p>
